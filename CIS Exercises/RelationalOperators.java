public class RelationalOperators {
    public static void main(String[] args) {
        // Comparison opertors are also sometimes 
        // called relational operators 

        int i = 3, j = 4;

        boolean flag; //default: set to false 


        // Setting flag value
        flag = 5 < 6;
        flag = (i == j);
        flag = (j + 2) <= 6;
        flag = (j + 2) <= 6;

        System.out.println(flag);
    }
}
